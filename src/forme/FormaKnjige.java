    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        String naziv=jTextFieldKnjiga.getText();
       String ISBN=jTextFieldISBN.getText();
       int godIzdanja;
        try{
           godIzdanja=Integer.parseInt(jTextFieldGodIzdanja.getText());
       }catch(NumberFormatException e){
           JOptionPane.showMessageDialog(this, "GODINA MORA BITI BROJ!", "GRESKA",JOptionPane.ERROR_MESSAGE);
           return;
       }
       
       Zanr zanr=(Zanr)jComboBoxZanr.getSelectedItem();
       Autor autor=(Autor)jComboBoxAutori.getSelectedItem();
       
       knjigaZaIzmenu.setNaziv(naziv);
       knjigaZaIzmenu.setAutor(autor);
       knjigaZaIzmenu.setGodinaIzdanja(godIzdanja);
       knjigaZaIzmenu.setZanr(zanr);
       
       kontroler.azurirajKnjigu(knjigaZaIzmenu);
       
       gf.osveziTabelu();
       JOptionPane.showMessageDialog(this, "KNJIGA USPESNO IZMENJENA", "USPESNO",JOptionPane.INFORMATION_MESSAGE);
       this.dispose();
    }//GEN-LAST:event_btnIzmeniActionPerformed

