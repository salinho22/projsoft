    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
       String naziv=jTextFieldKnjiga.getText();
       String ISBN=jTextFieldISBN.getText();
       int godIzdanja;
        try{
           godIzdanja=Integer.parseInt(jTextFieldGodIzdanja.getText());
       }catch(NumberFormatException e){
           JOptionPane.showMessageDialog(this, "GODINA MORA BITI BROJ!", "GRESKA",JOptionPane.ERROR_MESSAGE);
           return;
       }
       
       Zanr zanr=(Zanr)jComboBoxZanr.getSelectedItem();
       Autor autor=(Autor)jComboBoxAutori.getSelectedItem();
       Random rand=new Random();
       int id=101+rand.nextInt(Integer.MAX_VALUE-100);
       Knjiga novaKnjiga=new Knjiga(id,naziv,autor,godIzdanja,ISBN,zanr);
       
       kontroler.dodajKnjigu(novaKnjiga);
       gf.osveziTabelu();
       JOptionPane.showMessageDialog(this, "KNJIGA USPESNO DODATA", "USPESNO",JOptionPane.INFORMATION_MESSAGE);
       this.dispose();
    }//GEN-LAST:event_btnDodajActionPerformed

